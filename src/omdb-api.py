def df_of_movie_info(list_of_IMDbIDs):
    '''
    Input is a list of IMDb IDs
    Output is a dataframe with each row represeting a movie, the first column is
    the movie title, second column is a plot of the movie, and third column is
    the rotten tomatoes score of the movie.  Movie information is pulled from
    the OMDb API.
    '''
    df = pd.DataFrame(columns=['title', 'plot', 'rating'])
    for id in list_of_IMDbIDs:
        m_info = omdb.imdbid(id, fullplot=True)
        data = m_info['title'], m_info['plot'], m_info['ratings'][1]['value']
        temp_df = pd.DataFrame([data], columns=['title', 'plot', 'rating'])
        df = df.append(temp_df)
    return df.to_csv('movie_plot_df')


